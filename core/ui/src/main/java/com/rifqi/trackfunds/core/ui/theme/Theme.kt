@Composable
fun TrackFundsTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }

    val extendedColors = if (darkTheme) {
        ExtendedColors(
            income = IncomeDark,
            expense = ExpenseDark,
            accent = AccentGreenDark,
            onAccent = OnAccentGreen,
            chartConditionGood = ChartConditionGood,
            chartConditionWarning = ChartConditionWarning,
            chartConditionNeutral = ChartConditionNeutralDark,
            chartCategory1 = ChartCategory1,
            chartCategory2 = ChartCategory2,
            chartCategory3 = ChartCategory3,
            chartCategory4 = ChartCategory4,
            chartCategory5 = ChartCategory5,
            chartCategory6 = ChartCategory6
        )
    } else {
        ExtendedColors(
            income = IncomeLight,
            expense = ExpenseLight,
            accent = AccentGreenLight,
            onAccent = OnAccentGreen,
            chartConditionGood = ChartConditionGood,
            chartConditionWarning = ChartConditionWarning,
            chartConditionNeutral = ChartConditionNeutralLight,
            chartCategory1 = ChartCategory1,
            chartCategory2 = ChartCategory2,
            chartCategory3 = ChartCategory3,
            chartCategory4 = ChartCategory4,
            chartCategory5 = ChartCategory5,
            chartCategory6 = ChartCategory6
        )
    }


    CompositionLocalProvider(LocalExtendedColors provides extendedColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = AppTypography,
            shapes = AppShapes,
            content = content
        )
    }
}

object TrackFundsTheme {
    val extendedColors: ExtendedColors
        @Composable
        get() = LocalExtendedColors.current
}